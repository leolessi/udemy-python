# int é qualquer número inteiro, positivo ou negativo
print(11)
print(-11)
print(0)

# float representa qualquer número, positivo ou negativo, com ponto flutuante
# float sem sinal é considerado positivo
# SEMPRE deve-se usar ponto para separar as casas decimais
print(1.1, -1.11, sep=" | ")

# para descobrir o tipo
print(type(1.1), type(-1.11), type(0))
